// NewContSaUser :
func NewContSaUser(e *echo.Echo, useSaUser isauser.Usecase) {
	controller := &ContSaUser{
		useSaUser: useSaUser,
	}
	/*
		url := echoSwagger.URL("http://localhost:1323/swagger/doc.json") //The url pointing to API definition
	*/
	e.GET("/swagger/*", echoSwagger.WrapHandler)

	r := e.Group("/api/user")
	// Configure middleware with custom claims
	var screet = setting.FileConfigSetting.App.JwtSecret
	config := middleware.JWTConfig{
		Claims:     &util.Claims{},
		SigningKey: []byte(screet),
	}

	r.Use(middleware.JWTWithConfig(config))
	r.GET("/:id", controller.GetBySaUser)
	r.GET("/permission", controller.GetListPermission)
	r.GET("", controller.GetList)
	r.POST("", controller.CreateSaUser)
	r.PUT("/:id", controller.UpdateSaUser)
	r.DELETE("/:id", controller.DeleteSaUser)
	// r.GET("/api/user/:id", controller.GetBySaUser)
	// r.GET("/api/user/permission", controller.GetListPermission)
	// r.GET("/api/user", controller.GetList)
	// r.POST("/api/user", controller.CreateSaUser)
	// r.PUT("/api/user/:id", controller.UpdateSaUser)
	// r.DELETE("/api/user/:id", controller.DeleteSaUser)
}

